package week1.day2;

public class Calculator {
	
	public int add(int a, int b,int c)
	{
			return a+b+c;
	}
		
public int subtraction(int d, int e)
{
	return d-e;
	
	}

public int multiplication(int f,int g)
{
	return f*g;
	} 

public float division(float h,float j)
{
	return h/j;
	
}

	

}
